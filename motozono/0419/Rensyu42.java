public class Rensyu42{
	public static void main(String[] args){
		int j = 3;									//int j に３を代入
		//繰り返し
		for(int i = 0; i < 10; i++){ 				//int iに０を代入し、iは10より小さい。iに1ずつ足していく
			System.out.println(j * i);
		}
	}
}