public class rensyu1_10 {

	public static void main(String[] args){
		// 変数の宣言
		int x;

		x = 4;

		System.out.printf("x(%d)の1乗は%d\nx(%d)の2乗は%d\nx(%d)の3乗は%d\n", x, x, x, x * x, x, x * x * x);
	}
}
