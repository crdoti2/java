//------------------------------------------------------------------------------
//
//　int 型の変数 x に任意の数値を代入し、
//　x を 2 倍、3 倍、4 倍した結果を表示するプログラムを作成しなさい。
//
//------------------------------------------------------------------------------

public class rensyu1_9 {

	public static void main(String[] args){
		// 変数の宣言
		int x;
		int i;

		x = 2;

		for(i = 2; i <= 4; i++) {
			System.out.printf("x(%d)の%d倍は%d\n", x, i, x * i);
		}
	}
}
