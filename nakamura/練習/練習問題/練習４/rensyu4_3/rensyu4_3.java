//------------------------------------------------------------------------------
//
//　2 の 1 乗から 8 乗までを計算し表示するプログラムを作成しなさい。
//
//------------------------------------------------------------------------------

public class rensyu4_3 {

	public static void main(String[] args){
		// 変数の宣言
		int i, j;
		int n; 

		for(i = 1; i < 8; i++) {
			n = 2;
			for(j = 1; j < i; j++) {
				n *= 2;
			}
			System.out.printf("%3d\n", n);
		}
	}
}
