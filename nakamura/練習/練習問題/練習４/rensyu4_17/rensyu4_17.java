public class rensyu4_17 {

	public static void main(String[] args){
		// 変数の宣言
		int i;
		int j;

		for(i = 1; i < 10; i++) {
			for(j = 1; j < 10; j++) {
				System.out.printf("%3d", i * j);
			}
			System.out.println("");
		}
	}
}
