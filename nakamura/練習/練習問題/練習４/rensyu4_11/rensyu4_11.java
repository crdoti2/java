public class rensyu4_11 {

	public static void main(String[] args){
		// 変数の宣言
		int i;
		int n;

		System.out.printf("整数を入力 ＞ ");
		n = new java.util.Scanner(System.in).nextInt();

		for(i = 0; i < n; i++) {
			System.out.printf("%d", i % 10);
		}
	}
}
